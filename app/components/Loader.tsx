export default function Loader() {
  const overlayRef = useRef<HTMLDivElement>(null);
  const svgRef     = useRef<SVGSVGElement>(null);
  const pathRefs   = useRef<(SVGPathElement | null)[]>([]);

  // Hide all paths before first paint
  useLayoutEffect(() => {
    pathRefs.current.forEach((p) => {
      if (p) gsap.set(p, { opacity: 0, y: 5, force3D: true });
    });
  }, []);

  useEffect(() => {
    const overlay = overlayRef.current;
    const svg     = svgRef.current;
    if (!overlay || !svg) return;

    document.body.style.overflow = "hidden";

    const tl = gsap.timeline({
      onComplete: () => {
        document.body.style.overflow = "";
        overlay.style.pointerEvents = "none";
      },
    });

    // Letters rise in one by one — long duration + expo.out gives effortless deceleration
    tl.to(pathRefs.current, {
      opacity: 1,
      y: 0,
      duration: 0.8,
      ease: "expo.out",
      stagger: 0.06,
      force3D: true,
    });

    // Hold — let the full wordmark sit for a beat
    tl.to({}, { duration: 0.55 });

    // Overlay dissolves — letters stay exactly where they are as the white fades away
    tl.to(overlay, {
      opacity: 0,
      duration: 0.7,
      ease: "power1.inOut",
      onStart: () => document.dispatchEvent(new CustomEvent("bird:reveal")),
    });

    return () => {
      tl.kill();
      document.body.style.overflow = "";
    };
  }, []);

  return (
    <div
      ref={overlayRef}
      style={{
        position: "fixed",
        inset: 0,
        zIndex: 9999,
        background: "#ffffff",
        display: "flex",
        alignItems: "center",
        justifyContent: "center",
      }}
    >
      <svg
        ref={svgRef}
        width="409"
        height="51"
        viewBox="0 0 409 51"
        fill="none"
        xmlns="http://www.w3.org/2000/svg"
        aria-hidden="true"
        style={{ width: "clamp(200px, 40vw, 409px)", height: "auto" }}
      >
        {PATHS.map((d, i) => (
          <path
            key={i}
            ref={(el) => { pathRefs.current[i] = el; }}
            d={d}
            fill="#1E1E1E"
          />
        ))}
      </svg>
    </div>
  );
}
